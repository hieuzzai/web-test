function hover(i) {
    var colorChanged = document.getElementsByTagName("a");
    var logoFlat = document.getElementsByClassName("logo-flat")[0].style.background;
    colorChanged[i].style.color = '#1976D2';

    if (logoFlat == 'rgb(31, 86, 148)') {
        colorChanged[i].style.color = '#ff5722';
    }
    if (logoFlat == 'rgb(3, 129, 117)') {
        colorChanged[i].style.color = '#9C27B0';
    }
    if (logoFlat == 'rgb(97, 97, 97)') {
        colorChanged[i].style.color = '#F44336';
    }
    if (logoFlat == 'rgb(6, 167, 99)') {
        colorChanged[i].style.color = '#1976D2';
    }
    if (logoFlat == 'rgb(95, 26, 137)') {
        colorChanged[i].style.color = '#E61F63';
    }
    if (logoFlat == 'rgb(93, 64, 55') {
        colorChanged[i].style.color = '#3F51B5';
    }
    if (logoFlat == 'rgb(228, 36, 68)') {
        colorChanged[i].style.color = '#FF8F00';
    }
    if (logoFlat == 'rgb(207, 43, 126)') {
        colorChanged[i].style.color = '#9C27B0';
    }

}
